class Solution
{
    public:
    Node* reverseBetween(Node* head, int m, int n)
    {
        Node *current,*prev,*next;
        current = head;
        prev = next = NULL;
        
        Node *ctail,*thead,*ttail;
        ctail = thead = ttail = NULL;
        
        int count = 1;
        while(count!=m){
            count++;
            ctail = current;
            current = current->next;
        }
        ttail = current;
        while(count != n+1){
            next = current->next;
            current->next = prev;
            prev = current;
            current = next;
            count ++;
        }
        ttail->next = current;
        thead = prev;
        if(NULL==ctail){
            head = thead;
        }else{
            ctail->next = thead;
        }
        return head;
    }
};

